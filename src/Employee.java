public class Employee {
    int ID=0;
    String Name="";
    int Age=0;
    String Department="";
    double Salary=0.00;

    public Employee(int id, String nam, int age, String dep, double salary)
    {
        this.ID= id;
        this.Name=nam;
        this.Age=age;
        this.Department=dep;
        this.Salary=salary;
    }
}
